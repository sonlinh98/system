package jsoft.objects;

public class StatisticObject {

	private int statistic_id;
	private String statistic_current_date;
	private int statistic_visited;
	public int getStatistic_id() {
		return statistic_id;
	}
	public String getStatistic_current_date() {
		return statistic_current_date;
	}
	public int getStatistic_visited() {
		return statistic_visited;
	}
	public void setStatistic_id(int statistic_id) {
		this.statistic_id = statistic_id;
	}
	public void setStatistic_current_date(String statistic_current_date) {
		this.statistic_current_date = statistic_current_date;
	}
	public void setStatistic_visited(int statistic_visited) {
		this.statistic_visited = statistic_visited;
	}
	
	
	
}
